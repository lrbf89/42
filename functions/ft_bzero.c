void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*p;
	unsigned char	byte;

	i = 0;
	p = s;
	byte = '\0';
	while (i < n)
	{
		p[i] = byte;
		i++;
	}
}
